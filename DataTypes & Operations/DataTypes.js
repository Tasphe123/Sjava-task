// Number
let num = 41;
let floating = 7.5;

// Perform operations with num and floating
let addition = num + floating; // Addition
let subtraction = num - floating; // Subtraction
let multiplication = num * floating; // Multiplication
let division = num / floating; // Division
let modulus = num % floating; // Modulus
let exponentiation = num ** floating; // Exponentiation

// Print the results 
console.log("Addition: " + addition); // Output: Addition: 48.5
console.log("Subtraction: " + subtraction); // Output: Subtraction: 33.5
console.log("Multiplication: " + multiplication); // Output: Multiplication: 307.5
console.log("Division: " + division); // Output: Division: 5.466666666666667
console.log("Modulus: " + modulus); // Output: Modulus: 6.5
console.log("Exponentiation: " + exponentiation); // Output: Exponentiation: 1099511627776

// Number
let num1 = 5;
let num2 = 10;

// Comparison operations
let isEqual = num1 === num2; // Check if num1 is equal to num2
let isNotEqual = num1 !== num2; // Check if num1 is not equal to num2
let isGreater = num1 > num2; // Check if num1 is greater than num2
let isLess = num1 < num2; // Check if num1 is less than num2
let isGreaterOrEqual = num1 >= num2; // Check if num1 is greater than or equal to num2
let isLessOrEqual = num1 <= num2; // Check if num1 is less than or equal to num2

// Print the results of comparison operations
console.log("Is Equal: " + isEqual); // Output: false
console.log("Is Not Equal: " + isNotEqual); // Output: true
console.log("Is Greater: " + isGreater); // Output: false
console.log("Is Less: " + isLess); // Output: true
console.log("Is Greater or Equal: " + isGreaterOrEqual); // Output: false
console.log("Is Less or Equal: " + isLessOrEqual); // Output: true

